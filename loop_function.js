// 2. Create a new variable starting at a number of your choice
let startingNumber = 7;

// 3. Create a for loop that iterates 5 times.
for (let i = 0; i < 5; i++) {
    // If the variable from step 2 is odd, print it.
    if (startingNumber % 2 !== 0) {
        console.log("Odd Number:", startingNumber);
    }
    // Regardless of odd or even, increment it.
    startingNumber++;
}


